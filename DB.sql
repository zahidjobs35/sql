END_OF_CAL_YEAR	NUMBER(4),
END_OF_FIS_YEAR  NUMBER(4)
);


CREATE TABLE COSTS
(
PROD_ID  NUMBER(4),
TIME_ID    NUMBER(4),
UNIT_COST   NUMBER(12,2),
UNIT_PRICE  NUMBER(12,2) 
);


CREATE TABLE SALES
(
PROD_ID    NUMBER(4),
CUST_ID   NUMBER(4),
TIME_ID    NUMBER(4),
CHANNEL_ID   NUMBER(4) ,
PROMO_ID     NUMBER(4),
QUANTITY_SOLD    NUMBER(4),
AMOUNT_SOLD		NUMBER(4)
);



CREATE TABLE CHANNELS
(
CHANNEL_ID    NUMBER(4) PRIMARY KEY,
CHANNEL_DESCRIPTION   varchar2(150) ,
CHANNEL_CLASS		varchar2(100)
);


CREATE TABLE PRODUCTS
(
