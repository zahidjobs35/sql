


CREATE TABLE INDUSTRIALUNIT
(
UNITID  	number(4) PRIMARY KEY,
LOCATION	varchar2(50),
COUNTRY		varchar2(50)
);


CREATE TABLE Product 
(
ProdId			number(4) PRIMARY KEY,
Desc			number(4), 
StdCostPerUnit	number(12,2), 
StdLabCostPerUnit	number(12,2)
);


CREATE TABLE  IndUnitProduct 
(UnitId		number(4),
ProdId		number(4)
);



CREATE TABLE  Employee 
(EmpId			number(4) PRIMARY KEY, 
EmpName			varchar2(100), 
YtdEarnings		varchar2(12,2), 
YtdTax			varchar2(4),
YtdNICont		varchar2(4), 
UnitId			number(4),
TaxCode			number(4)
);



CREATE TABLE  WorkOrder 
(WorkOrderId		number(4) PRIMARY KEY, 
Qty					number(4), 
UnitPrice			number(12,2), 
OrderDate			Date, 
PromiseDate			Date, 
CompDate			Date, 
ShipDate			Date, 
Status				varchar2(4),
ProdID				number(4)
);



CREATE TABLE LabourUsage 
(EmpId			number(4),  
WorkOrderId		number(4), 
WorkDate		Date, 
Hours			number(2), 
HourlyRate		number(4),  
Posted			CHAR(1)
);


ALTER TABLE IndUnitProduct
ADD CONSTRAINT FK_UnitId
FOREIGN KEY (UnitId) REFERENCES INDUSTRIALUNIT(UnitId); 

ALTER TABLE IndUnitProduct
ADD CONSTRAINT FK_ProdId
FOREIGN KEY (ProdId) REFERENCES Product(ProdId); 


ALTER TABLE LabourUsage
ADD CONSTRAINT FK_LabourUsage
FOREIGN KEY (EmpId) REFERENCES Employee(EmpId); 


ALTER TABLE LabourUsage
ADD CONSTRAINT FK_LabourUsage_WorkOrderId
FOREIGN KEY (WorkOrderId) REFERENCES WorkOrder(WorkOrderId); 


ALTER TABLE WorkOrder
ADD CONSTRAINT FK_WorkOrder_ProdId
FOREIGN KEY (ProdId) REFERENCES Product(ProdId); 


CREATE TABLE PROMOTIONS (
PROMO_ID	NUMBER(4) PRIMARY KEY,
PROMO_NAME	VARCHAR2(100),
PROMO_CATEGORY	VARCHAR2(50),
PROMO_COST		NUMBER(12,2),
PROMO_BEGIN_DATE	dATE,
PROMO_END_DATE		DATE
);


CREATE TABLE TIMES
(
TIME_ID	NUMBER(4) PRIMARY KEY,
DAY_NAME	VARCHAR2(25),
DAY_NUMBER_IN_WEEK	NUMBER(4),
DAY_NUMBER_IN_MONTH	NUMBER(4),
CALENDER_WEEK_NUMBER	NUMBER(4),
FISCAL_WEEK_NUMBER		NUMBER(4),
WEEK_ENDING_DAY	VARCHAR2(100),
CALENDER_MONTH_NUMBER	NUMBER(4),
FISCAL_MONTH_NUMBER	NUMBER(4),
FISCAL_MONTH_DESC	NUMBER(4),
DAYS_IN_CAL_MONTH	NUMBER(4),
DAYS_IN_FIS_MONTH	NUMBER(4),
END_OF_CAL_MONTH	NUMBER(4),
END_OF_FIS_MONTH	NUMBER(4),
CALENDER_MONTH_NAME	VARCHAR2(100),
FISCAL_MONTH_NAME	VARCHAR2(100),
CALENDER_QUARTER_DESC	VARCHAR2(100),
FISCAL_QUARTER_DESC  VARCHAR2(100),
DAYS_IN_CAL_QUARTER	NUMBER(4),
DAYS_IN_FIS_QUARTER	NUMBER(4),
END_OF_CAL_QUARTER	NUMBER(4),
END_OF_FIS_QUARTER	NUMBER(4),
CALENDER_QUARTER_NUMBER   NUMBER(4),
